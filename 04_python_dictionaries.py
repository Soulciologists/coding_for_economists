#structure key-values
# Syntax:
mydict = {
  "key1": "value1",
  "key2": "value2",
  "key3": "value3",
}

# to access a value in a dictionary, usethe key:
print(mydict["key1"])

#add a key-value pair to a dictionary
mydict["key4"] = "value4"
print(mydict)

#to remove a key value pair to a dictionary
del mydict["key4"]